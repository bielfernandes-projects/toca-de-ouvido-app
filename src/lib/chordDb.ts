export function getChordShape(
  instrument: string,
  chordName: string,
): string | null {
  const direct = chordDb[instrument]?.[chordName];
  if (direct) return direct;

  const fallback = chordName.replace(/m7$/, "m");
  return fallback !== chordName
    ? (chordDb[instrument]?.[fallback] ?? null)
    : null;
}
